# asking the user to enter a number using while loops 

# ask user to enter number using the int(function),input(function)
number = int(input("Enter any number:"))
# created variables to complete the while loop function
even_numbers = 0 

# used the while loop to print all the even numbers and increment it by 2 
while even_numbers <= number:
      
      print(even_numbers)
      
      even_numbers = even_numbers + 2
      
      


      
      





     


    




  